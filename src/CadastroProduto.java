    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

        DefaultTableModel dtnProdutos = (DefaultTableModel) tbProdutos.getModel();
        dtnProdutos.setNumRows(0);
        if (txPesqId.getText().equals("") && txPesqNome.getText().equals("")) {
            List<Produto> produtos = ProdutoDAO.getProdutos();
            if (!produtos.isEmpty()) {
                for (Produto p : produtos) {
                    dtnProdutos.addRow(new Object[]{p.getId(), p.getNome(), p.getQuantidade(), p.getValor()});
                }
            }
        } else if (!txPesqId.getText().equals("") && txPesqNome.getText().equals("")) {
            Produto p = ProdutoDAO.getProdutoid(Integer.parseInt(txPesqId.getText()));
            dtnProdutos.addRow(new Object[]{p.getId(), p.getNome(), p.getQuantidade(), p.getValor()});

        } else if (txPesqId.getText().equals("") && !txPesqNome.getText().equals("")) {
            Produto p = ProdutoDAO.getProdutoNome(txPesqNome.getText());
            dtnProdutos.addRow(new Object[]{p.getId(), p.getNome(), p.getQuantidade(), p.getValor()});
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        int id = Integer.parseInt(txId.getText());
       
        if (id == 0) {
            Produto p = new Produto();
           
            p.setNome(txNomeProduto.getText());
            p.setQuantidade(Integer.parseInt(txQuantidadeProduto.getText()));
            p.setValor(Double.parseDouble(txValorProduto.getText()));

            try {
                ProdutoDAO.cadProduto(p);
                txId.setText("0");
                txNomeProduto.setText("");
                txQuantidadeProduto.setText("");
                txValorProduto.setText("");
                DefaultTableModel dtnProdutos = (DefaultTableModel) tbProdutos.getModel();
                dtnProdutos.setNumRows(0);
                if (txPesqId.getText().equals("") && txPesqNome.getText().equals("")) {
                    List<Produto> produtos = ProdutoDAO.getProdutos();
                    if (!produtos.isEmpty()) {
                        for (Produto p1 : produtos) {
                            dtnProdutos.addRow(new Object[]{p1.getId(), p1.getNome(), p1.getQuantidade(), p1.getValor()});
                        }
                    }
                }
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro!");
            } catch (SQLException ex) {
                Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro BD!");
            }
        } else if (id > 0) {
            Produto p = new Produto();
            //ProdutoDAO dao = new ProdutoDAO();
            p.setId(id);
            p.setNome(txNomeProduto.getText());
            p.setQuantidade(Integer.parseInt(txQuantidadeProduto.getText()));
            p.setValor(Double.parseDouble(txValorProduto.getText()));

            ProdutoDAO.updateProduto(p);
            txId.setText("0");
            txNomeProduto.setText("");
            txQuantidadeProduto.setText("");
            txValorProduto.setText("");

            DefaultTableModel dtnProdutos = (DefaultTableModel) tbProdutos.getModel();
            dtnProdutos.setNumRows(0);

            List<Produto> produtos = ProdutoDAO.getProdutos();
            if (!produtos.isEmpty()) {
                for (Produto p1 : produtos) {
                    dtnProdutos.addRow(new Object[]{p1.getId(), p1.getNome(), p1.getQuantidade(), p1.getValor()});
                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

