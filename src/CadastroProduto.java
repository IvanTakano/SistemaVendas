    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = tbProdutos.getSelectedRow();

        if (linhaSelecionada >= 0) {
            int a = Integer.parseInt(tbProdutos.getValueAt(linhaSelecionada, 0).toString());
            Produto p = ProdutoDAO.getProdutoid(a);
            String id = "" + p.getId();
            String quantidade = "" + p.getQuantidade();
            String valor = "" + p.getValor();

            txId.setText(id);
            txNomeProduto.setText(p.getNome());
            txQuantidadeProduto.setText(quantidade);
            txValorProduto.setText(valor);            
        }
    }//GEN-LAST:event_btnEditarActionPerformed

